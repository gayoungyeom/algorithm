#include <iostream>
using namespace std;

int main(){
	int n, ans;
	cin>>n;

	if(n < 10) ans = n;
	else if(n < 100) ans = 9 + (n - 9) * 2;
	else if(n < 1000) ans = 9 + 90 * 2 + (n - 99) * 3;
	else if(n < 10000) ans = 9 + 90 * 2 + 900 * 3 + (n - 999) * 4;
	else if(n < 100000) ans = 9 + 90 * 2 + 900 * 3 + 9000 * 4 + (n - 9999) * 5;
	else if(n < 1000000) ans = 9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + (n - 99999) * 6;
	else if(n < 10000000) ans = 9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + (n - 999999) * 7;
	else if(n < 100000000) ans = 9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + 9000000 * 7 + (n - 9999999) * 8;
	else ans = 9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + 9000000 * 7 + 90000000 * 8 + 9;

	cout<<ans<<'\n';

	return 0;
}


